package Math;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.abs(-9.5));
        System.out.println(Math.sqrt(5));

        System.out.println(Math.pow(2,3));
        System.out.println(Math.log(1));
        System.out.println(Math.exp(1.0));
        System.out.println(Math.max(-1.23,-1.4));
        System.out.println(Math.min(-1.34,-4.5));
        System.out.println(Math.ceil(-5.6));
        System.out.println(Math.ceil(5.6));
        System.out.println(Math.floor(-5.1));
        System.out.println(Math.floor(5.6)); //positive er jnno floor hcce doshomik er aga ja likah tai

    }
}
